#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*original_dest;

	original_dest = dest;
	while (*src != '\0' && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (original_dest);
}
